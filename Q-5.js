// Q5. Use the while loop to print numbers from 1 to 25 in ascending and descending order.

// -------Ascending---------

let numAscending = 1;
while (numAscending <= 25) {
    console.log(numAscending);
    numAscending++;
}

//----------Descending----------

let numDescending = 25
while (numDescending >= 1) {
    console.log(numDescending);
    numDescending = numDescending - 1

}