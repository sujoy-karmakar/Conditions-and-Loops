// Loops help us run a block of code repeatedly based on a condition. If the condition is false, the loop will end. If the condition is true, it will continue to run. Therefore, loops are very important in programming.
// In JavaScript there are mainly 3 type of loops :- do, while and do-while.


// for loop

for (let i = 0; i <= 10; i++) {
    console.log(i);
}


//while loop
let j = 0;
while (j <= 10) {
    console.log(j);
    j++;
}


//do-while loop
let k = 5;
do {
    console.log(k);
    k++;
} while (k <= 10)